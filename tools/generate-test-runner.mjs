#!/usr/bin/env node
import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
const testRoot = path.join(repoRoot, "test", "hyperopen");
const outputPath = path.join(repoRoot, "test", "test_runner_generated.cljs");

const toPosixPath = (value) => value.split(path.sep).join("/");

const collectTestFiles = async (dir) => {
  const entries = await readdir(dir, { withFileTypes: true });
  const sortedEntries = [...entries].sort((a, b) => a.name.localeCompare(b.name));
  const files = [];

  for (const entry of sortedEntries) {
    const fullPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      files.push(...(await collectTestFiles(fullPath)));
      continue;
    }

    if (entry.isFile() && entry.name.endsWith("_test.cljs")) {
      files.push(fullPath);
    }
  }

  return files;
};

const namespaceFromFile = (filePath) => {
  const relativePath = toPosixPath(path.relative(testRoot, filePath));
  const withoutExtension = relativePath.replace(/\.cljs$/, "");
  const namespaceTail = withoutExtension.replace(/\//g, ".").replace(/_/g, "-");
  return `hyperopen.${namespaceTail}`;
};

const renderRunTestsForm = (namespaces) => {
  if (namespaces.length === 0) {
    return "  (run-tests)";
  }

  const first = `  (run-tests '${namespaces[0]}`;
  const rest = namespaces.slice(1).map((namespace) => `             '${namespace}`);
  return [first, ...rest].join("\n") + ")";
};

const renderRunnerNamespace = (namespaces) => {
  const requireLines = namespaces
    .map((namespace) => `            [${namespace}]`)
    .join("\n");

  return `;; This file is generated by /hyperopen/tools/generate-test-runner.mjs.\n;; Do not edit by hand; run \`node tools/generate-test-runner.mjs\`.\n(ns test-runner-generated\n  (:require [cljs.test :refer-macros [run-tests]]\n${requireLines}))\n\n(defn run-generated-tests\n  \"Run all generated test namespaces and return the results.\"\n  []\n${renderRunTestsForm(namespaces)})\n`;
};

const main = async () => {
  const testFiles = await collectTestFiles(testRoot);
  const namespaces = [...new Set(testFiles.map(namespaceFromFile))].sort((a, b) =>
    a.localeCompare(b)
  );
  const output = renderRunnerNamespace(namespaces);

  await writeFile(outputPath, output, "utf8");
  console.log(`Generated ${path.relative(repoRoot, outputPath)} with ${namespaces.length} namespaces.`);
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
